'use client';

import React, { useMemo, useCallback } from 'react';
import {
  Sparkles,
  Globe,
  CheckCircle2,
  AlertTriangle,
  X,
  ChevronRight,
  FileText,
  Download,
  Link2,
} from 'lucide-react';
import { useSiteAudits } from '@/hooks/useApi';
import { getSessionId } from '@/lib/utils';
import { useResults, useResultsUI } from './ResultsContext';

export function RecommendationsTab() {
  const { runStatus, aiSummary, isSummaryLoading, isCategory, isPublicFigure, quickWins, adOpportunities, sourceOpportunities, parsedAiRecommendations } = useResults();
  const { setActiveTab, copied, handleCopyLink } = useResultsUI();

  const handleExportRecommendationsPDF = useCallback((recommendations: Array<{
    title: string;
    description: string;
    impact: string;
    effort: string;
    tactics: string[];
  }>) => {
    if (!runStatus || recommendations.length === 0) return;

    const getQuadrantName = (impact: string, effort: string) => {
      if (impact === 'high' && effort === 'low') return 'Quick Win';
      if (impact === 'high' && effort === 'high') return 'Major Project';
      if (impact === 'low' && effort === 'low') return 'Low Priority';
      if (impact === 'low' && effort === 'high') return 'Avoid';
      return 'Consider';
    };

    // Build PDF directly with jsPDF (no html2canvas dependency)
    import('jspdf').then((jsPDFModule) => {
      const jsPDF = jsPDFModule.default;
      const doc = new jsPDF({ orientation: 'portrait', unit: 'mm', format: 'a4' });
      const pageWidth = doc.internal.pageSize.getWidth();
      const pageHeight = doc.internal.pageSize.getHeight();
      const margin = 20;
      const contentWidth = pageWidth - margin * 2;
      let y = margin;

      const checkPageBreak = (needed: number) => {
        if (y + needed > pageHeight - margin) {
          doc.addPage();
          y = margin;
        }
      };

      // Title
      doc.setFontSize(20);
      doc.setFont('helvetica', 'bold');
      doc.setTextColor(17, 24, 39);
      doc.text(runStatus.search_type === 'category' ? 'AI Industry Analysis' : runStatus.search_type === 'public_figure' ? 'AI Reputation Recommendations' : 'AI Visibility Recommendations', margin, y);
      y += 8;

      // Subtitle
      doc.setFontSize(11);
      doc.setFont('helvetica', 'normal');
      doc.setTextColor(107, 114, 128);
      doc.text(`${runStatus.brand} — Generated ${new Date().toLocaleDateString()}`, margin, y);
      y += 12;

      // Recommendations
      recommendations.forEach((rec, idx) => {
        checkPageBreak(40);

        // Rec title
        doc.setFontSize(13);
        doc.setFont('helvetica', 'bold');
        doc.setTextColor(17, 24, 39);
        const titleLines = doc.splitTextToSize(`${idx + 1}. ${rec.title}`, contentWidth);
        doc.text(titleLines, margin, y);
        y += titleLines.length * 6 + 2;

        // Description
        doc.setFontSize(10);
        doc.setFont('helvetica', 'normal');
        doc.setTextColor(75, 85, 99);
        const descLines = doc.splitTextToSize(rec.description, contentWidth);
        checkPageBreak(descLines.length * 5);
        doc.text(descLines, margin, y);
        y += descLines.length * 5 + 3;

        // Meta badges
        doc.setFontSize(9);
        doc.setFont('helvetica', 'bold');
        const quadrant = getQuadrantName(rec.impact, rec.effort);
        doc.setTextColor(107, 114, 128);
        doc.text(`Impact: ${rec.impact}  •  Effort: ${rec.effort}  •  ${quadrant}`, margin, y);
        y += 6;

        // Tactics
        if (rec.tactics.length > 0) {
          doc.setFontSize(9);
          doc.setFont('helvetica', 'bold');
          doc.setTextColor(107, 114, 128);
          doc.text('Tactics:', margin, y);
          y += 5;

          doc.setFont('helvetica', 'normal');
          doc.setTextColor(75, 85, 99);
          rec.tactics.forEach((t) => {
            checkPageBreak(6);
            const tacticLines = doc.splitTextToSize(`•  ${t}`, contentWidth - 5);
            doc.text(tacticLines, margin + 3, y);
            y += tacticLines.length * 4.5 + 1;
          });
        }

        // Separator line
        y += 3;
        checkPageBreak(4);
        doc.setDrawColor(229, 231, 235);
        doc.setLineWidth(0.3);
        doc.line(margin, y, pageWidth - margin, y);
        y += 8;
      });

      // Footer
      checkPageBreak(10);
      doc.setFontSize(8);
      doc.setFont('helvetica', 'normal');
      doc.setTextColor(156, 163, 175);
      doc.text('Generated by AI Visibility Tracker', margin, y);

      doc.save(`${runStatus.search_type === 'category' ? 'analysis' : runStatus.search_type === 'public_figure' ? 'reputation-recommendations' : 'recommendations'}-${runStatus.brand}-${new Date().toISOString().split('T')[0]}.pdf`);
    });
  }, [runStatus]);

  const handleExportRecommendationsCSV = useCallback((recommendations: Array<{
    title: string;
    description: string;
    impact: string;
    effort: string;
    tactics: string[];
  }>) => {
    if (!runStatus || recommendations.length === 0) return;

    const headers = [
      'Recommendation',
      'Description',
      'Impact',
      'Effort',
      'Category',
      'Tactics',
    ];

    const rows = recommendations.map(rec => {
      const quadrantName =
        rec.impact === 'high' && rec.effort === 'low' ? 'Quick Win' :
        rec.impact === 'high' && rec.effort === 'high' ? 'Major Project' :
        rec.impact === 'low' && rec.effort === 'low' ? 'Low Priority' :
        rec.impact === 'low' && rec.effort === 'high' ? 'Avoid' : 'Consider';

      return [
        `"${rec.title.replace(/"/g, '""')}"`,
        `"${rec.description.replace(/"/g, '""')}"`,
        rec.impact,
        rec.effort,
        quadrantName,
        `"${rec.tactics.join('; ').replace(/"/g, '""')}"`,
      ];
    });

    const csv = [headers.join(','), ...rows.map(r => r.join(','))].join('\n');
    const blob = new Blob([csv], { type: 'text/csv' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = `${runStatus.search_type === 'category' ? 'analysis' : runStatus.search_type === 'public_figure' ? 'reputation-recommendations' : 'recommendations'}-${runStatus.brand}-${runStatus.run_id.slice(0, 8)}.csv`;
    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
    URL.revokeObjectURL(url);
  }, [runStatus]);

  // Fetch site audits for this session
  const sessionId = getSessionId();
  const { data: siteAuditsData } = useSiteAudits(sessionId);
  const siteAudits = siteAuditsData?.audits || [];

  // Find the most recent completed audit, preferring one that matches the current run's brand
  const latestAudit = useMemo(() => {
    const completed = siteAudits.filter(a => a.status === 'complete' && a.overall_score != null);
    if (completed.length === 0) return null;
    // Sort by created_at descending
    const sorted = completed.sort((a, b) =>
      new Date(b.created_at).getTime() - new Date(a.created_at).getTime()
    );
    // Prefer an audit whose URL contains the current run's brand name
    if (runStatus?.brand) {
      const brandLower = runStatus.brand.toLowerCase().replace(/\s+/g, '');
      const brandMatch = sorted.find(a => {
        try {
          const hostname = new URL(a.url).hostname.toLowerCase().replace(/^www\./, '');
          return hostname.includes(brandLower) || brandLower.includes(hostname.split('.')[0]);
        } catch {
          return a.url.toLowerCase().includes(brandLower);
        }
      });
      if (brandMatch) return brandMatch;
    }
    return sorted[0];
  }, [siteAudits, runStatus?.brand]);

  // Show loading state while AI summary is being generated
  if (isSummaryLoading) {
    return (
      <div className="space-y-6">
        <div className="bg-white rounded-xl shadow-sm border border-gray-100 p-6">
          <div className="flex items-center gap-3 mb-4">
            <div className="w-10 h-10 bg-gray-100 rounded-lg flex items-center justify-center">
              <Sparkles className="w-5 h-5 text-gray-900" />
            </div>
            <div>
              <h2 className="text-lg font-semibold text-gray-900">{isCategory ? 'AI Industry Analysis' : isPublicFigure ? 'AI Reputation Recommendations' : 'AI-Powered Recommendations'}</h2>
              <p className="text-sm text-gray-500">{isCategory ? 'Generating industry intelligence...' : isPublicFigure ? 'Generating reputation strategy...' : 'Generating personalized strategy...'}</p>
            </div>
          </div>
          <div className="bg-gray-50 rounded-lg p-8">
            <div className="flex flex-col items-center justify-center">
              <div className="animate-spin rounded-full h-8 w-8 border-b-2 border-gray-900 mb-4"></div>
              <p className="text-sm text-gray-500">{isCategory ? 'Analyzing industry data and generating insights...' : isPublicFigure ? 'Analyzing AI portrayal data and generating reputation recommendations...' : 'Analyzing your visibility data and generating recommendations...'}</p>
            </div>
          </div>
        </div>
      </div>
    );
  }

  return (
    <div className="space-y-6">
      {/* AI-Powered Recommendations - combined section with chart + cards */}
      {aiSummary?.recommendations && parsedAiRecommendations.length > 0 && !isCategory && (
      <div id="recommendations-ai" className="bg-white rounded-xl shadow-sm border border-gray-100 p-6">

        {/* Effort vs Impact Matrix Chart — hidden for industry reports */}
        {parsedAiRecommendations.length > 0 && (() => {
          // Calculate positions and handle overlapping circles
          const effortMap = { low: 16.67, medium: 50, high: 83.33 };
          const impactMap = { low: 83.33, medium: 50, high: 16.67 };

          // Group items by position to detect overlaps
          const positionGroups: Record<string, number[]> = {};
          parsedAiRecommendations.forEach((rec, idx) => {
            const key = `${rec.effort}-${rec.impact}`;
            if (!positionGroups[key]) positionGroups[key] = [];
            positionGroups[key].push(idx);
          });

          // Calculate offset for each item - stack vertically when overlapping
          const getOffset = (idx: number, rec: typeof parsedAiRecommendations[0]) => {
            const key = `${rec.effort}-${rec.impact}`;
            const group = positionGroups[key];
            const posInGroup = group.indexOf(idx);
            const total = group.length;
            if (total === 1) return { x: 0, y: 0 };
            // Stack items vertically with 28px spacing to prevent label overlap
            const verticalSpacing = 28;
            const totalHeight = (total - 1) * verticalSpacing;
            const startY = -totalHeight / 2;
            return { x: 0, y: startY + posInGroup * verticalSpacing };
          };

          return (
          <div className="mb-6">
            {/* Chart Title */}
            <div className="flex items-center gap-3 mb-4">
              <div className="w-10 h-10 bg-gray-100 rounded-lg flex items-center justify-center">
                <Sparkles className="w-5 h-5 text-gray-900" />
              </div>
              <div>
                <h2 className="text-lg font-semibold text-gray-900">{isCategory ? 'Insight Priority Matrix' : 'Effort vs Impact Matrix'}</h2>
                <p className="text-sm text-gray-500">{isCategory ? 'Key industry insights ranked by significance and actionability' : isPublicFigure ? 'Prioritize reputation strategies by their expected impact and implementation effort' : 'Prioritize recommendations by their expected impact and implementation effort'}</p>
              </div>
            </div>
            <div className="relative bg-white rounded-lg p-4">
              {/* Chart container */}
              <div className="ml-8">
                {/* Y-axis labels */}
                <div className="flex">
                  {/* Y-axis label (rotated) - positioned to align with grid center */}
                  <div className="relative w-6 flex items-center justify-center" style={{ height: '315px' }}>
                    <span className="-rotate-90 text-base font-semibold text-gray-600 whitespace-nowrap">
                      Impact
                    </span>
                  </div>
                  <div className="w-12 flex flex-col justify-between text-right pr-2 text-sm text-gray-400" style={{ height: '315px' }}>
                    <span>High</span>
                    <span>Med</span>
                    <span>Low</span>
                  </div>

                  {/* Grid */}
                  <div className="flex-1 relative" style={{ height: '315px' }}>
                    {/* Background grid - simplified */}
                    <div className="absolute inset-0 grid grid-cols-3 grid-rows-3">
                      {/* Quick Wins quadrant (top-left) - label in top-left corner */}
                      <div className="bg-[#E8F5E9] border border-gray-100 relative">
                        <span className="absolute top-1.5 left-1.5 text-xs text-gray-900 font-medium opacity-70">Quick Wins</span>
                      </div>
                      <div className="bg-[#E8F5E9]/50 border border-gray-100" />
                      {/* Major Projects quadrant (top-right) - label in top-right corner */}
                      <div className="bg-amber-50/70 border border-gray-100 relative">
                        <span className="absolute top-1.5 right-1.5 text-xs text-amber-600 font-medium opacity-70">Major Projects</span>
                      </div>
                      <div className="bg-gray-50 border border-gray-100" />
                      <div className="bg-gray-50 border border-gray-100" />
                      <div className="bg-gray-50 border border-gray-100" />
                      {/* Low Priority quadrant (bottom-left) - label in bottom-left corner */}
                      <div className="bg-blue-50/50 border border-gray-100 relative">
                        <span className="absolute bottom-1.5 left-1.5 text-xs text-blue-500 font-medium opacity-70">Low Priority</span>
                      </div>
                      <div className="bg-gray-50/50 border border-gray-100" />
                      {/* Avoid quadrant (bottom-right) - label in bottom-right corner */}
                      <div className="bg-red-50/50 border border-gray-100 relative">
                        <span className="absolute bottom-1.5 right-1.5 text-xs text-red-400 font-medium opacity-70">Avoid</span>
                      </div>
                    </div>

                    {/* Plot points with labels */}
                    {parsedAiRecommendations.map((rec, idx) => {
                      const x = effortMap[rec.effort];
                      const y = impactMap[rec.impact];
                      const offset = getOffset(idx, rec);

                      // Build detailed tooltip
                      const quadrantName =
                        rec.impact === 'high' && rec.effort === 'low' ? 'Quick Win' :
                        rec.impact === 'high' && rec.effort === 'high' ? 'Major Project' :
                        rec.impact === 'low' && rec.effort === 'low' ? 'Low Priority' :
                        rec.impact === 'low' && rec.effort === 'high' ? 'Avoid' : 'Consider';

                      const tooltip = `${rec.title}

${quadrantName.toUpperCase()}

Impact: ${rec.impact.charAt(0).toUpperCase() + rec.impact.slice(1)}
↳ ${rec.impactReason}

Effort: ${rec.effort.charAt(0).toUpperCase() + rec.effort.slice(1)}
↳ ${rec.effortReason}`;

                      return (
                        <div
                          key={idx}
                          className="absolute cursor-default group"
                          style={{
                            left: `${x}%`,
                            top: `${y}%`,
                            transform: `translate(calc(-50% + ${offset.x}px), calc(-50% + ${offset.y}px))`,
                            zIndex: 10 + idx
                          }}
                          title={tooltip}
                        >
                          {/* Dot with label */}
                          <div className="flex items-center gap-1.5 relative">
                            <div className="w-3 h-3 bg-gray-900 rounded-full shadow-sm border border-white flex-shrink-0 group-hover:scale-125 transition-transform duration-150" />
                            <span className="text-xs font-medium text-gray-700 bg-white/90 px-1.5 py-0.5 rounded shadow-sm overflow-hidden text-ellipsis whitespace-nowrap max-w-[180px] group-hover:max-w-none group-hover:bg-gray-900 group-hover:text-white transition-all duration-150">
                              {rec.title}
                            </span>
                          </div>
                        </div>
                      );
                    })}
                  </div>
                </div>

                {/* X-axis labels */}
                <div className="flex mt-2">
                  <div className="w-[72px]" /> {/* Spacer to align with grid (w-6 Impact label + w-12 Y-axis values) */}
                  <div className="flex-1 grid grid-cols-3 text-center text-sm text-gray-400">
                    <span>Low</span>
                    <span>Medium</span>
                    <span>High</span>
                  </div>
                </div>

                {/* X-axis label */}
                <div className="flex mt-1">
                  <div className="w-[72px]" />
                  <div className="flex-1 text-center text-base font-semibold text-gray-600">
                    Effort
                  </div>
                </div>
              </div>

            </div>
          </div>
          );
        })()}

      </div>
      )}

        {/* Recommendation Cards - separate section */}
        {parsedAiRecommendations.length > 0 && (
          <div id="recommendations-cards" className="bg-white rounded-xl shadow-sm border border-gray-100 p-6">
            <div className="flex items-center gap-3 mb-5">
              <div className="w-10 h-10 bg-gray-100 rounded-lg flex items-center justify-center">
                <Sparkles className="w-5 h-5 text-gray-900" />
              </div>
              <div>
                <h2 className="text-lg font-semibold text-gray-900">{isCategory ? 'Industry Intelligence Insights' : isPublicFigure ? 'AI Reputation Recommendations' : 'AI-Powered Recommendations'}</h2>
                <p className="text-sm text-gray-500">{isCategory ? 'How AI platforms are shaping brand discovery in this industry' : isPublicFigure ? 'Strategies to improve this figure\'s image and portrayal in AI search results' : 'Personalized strategy based on your visibility analysis'}</p>
              </div>
            </div>
            <div className="space-y-3">
            {parsedAiRecommendations.map((rec, idx) => {
              const quadrantName =
                rec.impact === 'high' && rec.effort === 'low' ? 'Quick Win' :
                rec.impact === 'high' && rec.effort === 'high' ? 'Major Project' :
                rec.impact === 'low' && rec.effort === 'low' ? 'Low Priority' :
                rec.impact === 'low' && rec.effort === 'high' ? 'Avoid' : 'Consider';

              const impactBadge: Record<string, string> = {
                high: 'bg-emerald-50 text-emerald-700 border border-emerald-200',
                medium: 'bg-amber-50 text-amber-700 border border-amber-200',
                low: 'bg-gray-50 text-gray-500 border border-gray-200',
              };

              const effortBadge: Record<string, string> = {
                low: 'bg-blue-50 text-blue-700 border border-blue-200',
                medium: 'bg-orange-50 text-orange-600 border border-orange-200',
                high: 'bg-red-50 text-red-600 border border-red-200',
              };

              const categoryStyle: Record<string, { badge: string; border: string; numBg: string }> = {
                'Quick Win': { badge: 'bg-emerald-50 text-emerald-700 border border-emerald-200', border: 'border-l-emerald-400', numBg: 'bg-emerald-600' },
                'Major Project': { badge: 'bg-amber-50 text-amber-700 border border-amber-200', border: 'border-l-amber-400', numBg: 'bg-amber-500' },
                'Low Priority': { badge: 'bg-blue-50 text-blue-600 border border-blue-200', border: 'border-l-blue-400', numBg: 'bg-blue-500' },
                'Avoid': { badge: 'bg-red-50 text-red-600 border border-red-200', border: 'border-l-red-400', numBg: 'bg-red-500' },
                'Consider': { badge: 'bg-gray-100 text-gray-700 border border-gray-200', border: 'border-l-gray-400', numBg: 'bg-gray-600' },
              };

              const style = categoryStyle[quadrantName] || categoryStyle['Consider'];

              const insightNumColors = [
                'bg-emerald-600', 'bg-blue-600', 'bg-amber-500', 'bg-purple-600',
                'bg-rose-500', 'bg-teal-600', 'bg-indigo-500', 'bg-orange-500',
              ];
              const numBgColor = isCategory ? insightNumColors[idx % insightNumColors.length] : style.numBg;

              return (
                <div
                  key={idx}
                  className={`rounded-xl border border-gray-100 ${isCategory ? '' : `border-l-[3px] ${style.border}`} bg-white p-5 hover:shadow-md transition-shadow`}
                >
                  {/* Title row with number badge and pill badges */}
                  <div className={`flex items-start gap-4 ${isCategory ? '' : 'justify-between'}`}>
                    <div className="flex items-start gap-3 flex-1 min-w-0">
                      <div className={`w-7 h-7 ${numBgColor} rounded-full flex items-center justify-center flex-shrink-0 mt-0.5`}>
                        <span className="text-white text-xs font-bold">{idx + 1}</span>
                      </div>
                      <div className="flex-1">
                        <h3 className="text-sm font-semibold text-gray-900 leading-snug">{rec.title}</h3>
                        {rec.description && (
                          <p className="text-sm text-gray-500 mt-1.5 leading-relaxed">{rec.description}</p>
                        )}
                      </div>
                    </div>
                    {!isCategory && (
                      <div className="flex items-center gap-2 flex-shrink-0 flex-wrap justify-end">
                        <span
                          className={`px-2 py-0.5 rounded-full text-xs font-medium ${impactBadge[rec.impact] || ''}`}
                          title={rec.impactReason ? `Impact: ${rec.impactReason}` : undefined}
                        >
                          {rec.impact.charAt(0).toUpperCase() + rec.impact.slice(1)} Impact
                        </span>
                        <span
                          className={`px-2 py-0.5 rounded-full text-xs font-medium ${effortBadge[rec.effort] || ''}`}
                          title={rec.effortReason ? `Effort: ${rec.effortReason}` : undefined}
                        >
                          {rec.effort.charAt(0).toUpperCase() + rec.effort.slice(1)} Effort
                        </span>
                        <span className={`px-2 py-0.5 rounded-full text-xs font-medium ${style.badge}`}>
                          {quadrantName}
                        </span>
                      </div>
                    )}
                  </div>

                  {/* Tactics checklist */}
                  {rec.tactics && rec.tactics.length > 0 && (
                    <div className="mt-3 ml-10 space-y-2">
                      {rec.tactics.map((tactic, tidx) => (
                        <div key={tidx} className="flex items-start gap-2 text-sm text-gray-600">
                          <CheckCircle2 className="w-4 h-4 text-gray-300 mt-0.5 flex-shrink-0" />
                          <span className="flex-1">{tactic}</span>
                        </div>
                      ))}
                    </div>
                  )}
                </div>
              );
            })}
            </div>
          </div>
        )}

      {/* Site Audit Insights — hidden for category/industry reports */}
      {!isCategory && <div id="recommendations-llm" className="bg-white rounded-xl shadow-sm border border-gray-100 p-6">
        <div className="flex items-center gap-3 mb-4">
          <div className="w-10 h-10 bg-gray-100 rounded-lg flex items-center justify-center">
            <Globe className="w-5 h-5 text-gray-900" />
          </div>
          <div>
            <h2 className="text-lg font-semibold text-gray-900">{isPublicFigure ? 'Online Presence Optimization' : 'Website AI Optimization'}</h2>
            <p className="text-sm text-gray-500">{isPublicFigure ? 'Technical factors affecting how AI portrays this figure' : 'Technical factors affecting your AI visibility'}</p>
          </div>
        </div>

        {latestAudit ? (
          <div className="space-y-4">
            {/* Audit Summary */}
            <div className="bg-gray-50 rounded-lg p-4">
              <div className="flex items-center justify-between mb-3">
                <div>
                  <p className="text-sm text-gray-500">Latest audit for</p>
                  <p className="font-medium text-gray-900">{latestAudit.url}</p>
                </div>
                <div className={`px-4 py-2 rounded-lg ${
                  (latestAudit.overall_score ?? 0) >= 90 ? 'bg-gray-100' :
                  (latestAudit.overall_score ?? 0) >= 70 ? 'bg-gray-100' :
                  (latestAudit.overall_score ?? 0) >= 50 ? 'bg-yellow-100' : 'bg-red-100'
                }`}>
                  <span className={`text-2xl font-bold ${
                    (latestAudit.overall_score ?? 0) >= 90 ? 'text-gray-900' :
                    (latestAudit.overall_score ?? 0) >= 70 ? 'text-gray-700' :
                    (latestAudit.overall_score ?? 0) >= 50 ? 'text-yellow-600' : 'text-red-600'
                  }`}>
                    {latestAudit.overall_score}
                  </span>
                  <span className="text-gray-500 text-sm ml-1">/100</span>
                </div>
              </div>

              {/* Key findings from the audit */}
              {latestAudit.results && (
                <div className="grid sm:grid-cols-2 lg:grid-cols-3 gap-3 mt-4">
                  {/* AI Crawler Access */}
                  {latestAudit.results.robots_txt && (
                    <div className="bg-white rounded-lg p-3 border border-gray-200">
                      <p className="text-xs text-gray-500 mb-1">AI Bot Access</p>
                      <div className="flex items-center gap-2">
                        {latestAudit.results.robots_txt.crawlers.every(c => c.allowed) ? (
                          <>
                            <CheckCircle2 className="w-4 h-4 text-gray-600" />
                            <span className="text-sm font-medium text-gray-700">All allowed</span>
                          </>
                        ) : (
                          <>
                            <AlertTriangle className="w-4 h-4 text-yellow-500" />
                            <span className="text-sm font-medium text-yellow-700">
                              {latestAudit.results.robots_txt.crawlers.filter(c => !c.allowed).length} blocked
                            </span>
                          </>
                        )}
                      </div>
                    </div>
                  )}

                  {/* Structured Data */}
                  {latestAudit.results.structured_data && (
                    <div className="bg-white rounded-lg p-3 border border-gray-200">
                      <p className="text-xs text-gray-500 mb-1">Rich Data Markup</p>
                      <div className="flex items-center gap-2">
                        {latestAudit.results.structured_data.has_json_ld ? (
                          <>
                            <CheckCircle2 className="w-4 h-4 text-gray-600" />
                            <span className="text-sm font-medium text-gray-700">
                              {latestAudit.results.structured_data.json_ld_types.slice(0, 2).join(', ')}
                            </span>
                          </>
                        ) : (
                          <>
                            <X className="w-4 h-4 text-red-500" />
                            <span className="text-sm font-medium text-red-700">Missing rich data markup</span>
                          </>
                        )}
                      </div>
                    </div>
                  )}

                  {/* Content Accessibility */}
                  {latestAudit.results.content_accessibility && (
                    <div className="bg-white rounded-lg p-3 border border-gray-200">
                      <p className="text-xs text-gray-500 mb-1">Content Accessibility</p>
                      <div className="flex items-center gap-2">
                        {latestAudit.results.content_accessibility.estimated_ssr ? (
                          <>
                            <CheckCircle2 className="w-4 h-4 text-gray-600" />
                            <span className="text-sm font-medium text-gray-700">Fully readable by AI</span>
                          </>
                        ) : (
                          <>
                            <AlertTriangle className="w-4 h-4 text-yellow-500" />
                            <span className="text-sm font-medium text-yellow-700">May be hard for AI to read</span>
                          </>
                        )}
                      </div>
                    </div>
                  )}
                </div>
              )}

              {/* Audit recommendations */}
              {latestAudit.recommendations && latestAudit.recommendations.length > 0 && (
                <div className="mt-4 pt-4 border-t border-gray-200">
                  <p className="text-sm font-medium text-gray-700 mb-2">Top Technical Recommendations</p>
                  <ul className="space-y-2">
                    {latestAudit.recommendations.slice(0, 3).map((rec, idx) => (
                      <li key={idx} className="flex items-start gap-2 text-sm">
                        <span className={`px-1.5 py-0.5 rounded text-xs font-medium ${
                          rec.priority === 'high' ? 'bg-red-100 text-red-700' :
                          rec.priority === 'medium' ? 'bg-yellow-100 text-yellow-700' :
                          'bg-blue-100 text-blue-700'
                        }`}>
                          {rec.priority === 'high' ? 'High' : rec.priority === 'medium' ? 'Med' : 'Low'}
                        </span>
                        <span className="text-gray-700">{rec.title}</span>
                      </li>
                    ))}
                  </ul>
                </div>
              )}
            </div>

            <button
              onClick={() => setActiveTab('site-audit')}
              className="text-sm text-gray-900 hover:underline flex items-center gap-1"
            >
              View full site audit details
              <ChevronRight className="w-4 h-4" />
            </button>
          </div>
        ) : (
          <div className="bg-gray-50 rounded-lg p-6 text-center">
            <Globe className="w-10 h-10 text-gray-300 mx-auto mb-3" />
            <p className="text-gray-700 font-medium mb-2">No site audit yet</p>
            <p className="text-sm text-gray-500 mb-4">
              {isPublicFigure
                ? `Run a site audit to check if ${runStatus?.brand}'s online presence is optimized for AI platforms like ChatGPT, Claude, and Perplexity.`
                : `Run a site audit to check if ${runStatus?.brand}'s website is optimized for AI search engines like ChatGPT, Claude, and Perplexity.`}
            </p>
            <button
              onClick={() => setActiveTab('site-audit')}
              className="px-4 py-2 bg-gray-900 text-white text-sm font-medium rounded-lg hover:bg-gray-800 transition-colors inline-flex items-center gap-2"
            >
              <Globe className="w-4 h-4" />
              Run Site Audit
            </button>
          </div>
        )}
      </div>}

      {/* Export & Share Footer */}
      <div className="bg-white rounded-xl shadow-sm border border-gray-100 overflow-hidden">
        <div className="flex items-center justify-end gap-3 p-4 bg-gray-50/50 border-t border-gray-100">
          <button
            onClick={() => handleExportRecommendationsPDF(parsedAiRecommendations)}
            disabled={parsedAiRecommendations.length === 0}
            className="px-3 py-1.5 border border-gray-200 text-gray-700 text-sm font-medium rounded-lg hover:bg-gray-50 transition-colors flex items-center gap-1.5 disabled:opacity-50 disabled:cursor-not-allowed"
          >
            <FileText className="w-4 h-4" />
            Export PDF
          </button>
          <button
            onClick={() => handleExportRecommendationsCSV(parsedAiRecommendations)}
            disabled={parsedAiRecommendations.length === 0}
            className="px-3 py-1.5 border border-gray-200 text-gray-700 text-sm font-medium rounded-lg hover:bg-gray-50 transition-colors flex items-center gap-1.5 disabled:opacity-50 disabled:cursor-not-allowed"
          >
            <Download className="w-4 h-4" />
            Export CSV
          </button>
          <button
            onClick={handleCopyLink}
            className="px-3 py-1.5 border border-gray-200 text-gray-700 text-sm font-medium rounded-lg hover:bg-gray-50 transition-colors flex items-center gap-1.5"
          >
            <Link2 className="w-4 h-4" />
            {copied ? 'Copied!' : 'Share'}
          </button>
        </div>
      </div>
    </div>
  );
}
